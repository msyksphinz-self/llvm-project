#ifndef LLVM_LIB_TARGET_MYRISCVX_INSTPRINTER_MYRISCVXINSTPRINTER_H
#define LLVM_LIB_TARGET_MYRISCVX_INSTPRINTER_MYRISCVXINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
// These enumeration declarations were orignally in MYRISCVXInstrInfo.h but
// had to be moved here to avoid circular dependencies between
// LLVMMYRISCVXCodeGen and LLVMMYRISCVXAsmPrinter.

class TargetMachine;

class MYRISCVXInstPrinter : public MCInstPrinter {
 public:
  MYRISCVXInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                      const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;

  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address,
                 StringRef Annot, const MCSubtargetInfo &STI,
                 raw_ostream &O) override;

  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address, unsigned OpIdx,
                               unsigned PrintMethodIdx, raw_ostream &O);

 private:
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printOperand(const MCInst *MI, uint64_t /*Address*/, unsigned OpNum,
                    raw_ostream &O) {
    printOperand(MI, OpNum, O);
  }
  void printUnsignedImm(const MCInst *MI, int opNum, raw_ostream &O);
  void printMemOperand(const MCInst *MI, int opNum, raw_ostream &O);
};
} // end namespace llvm

#endif
